package xin.liaozhixing.class14ba2.domain;

public class Module {
	private Integer module_id;
	private String module_name;
	private Long module_access_num;
	public Integer getModule_id() {
		return module_id;
	}
	public void setModule_id(Integer module_id) {
		this.module_id = module_id;
	}
	public String getModule_name() {
		return module_name;
	}
	public void setModule_name(String module_name) {
		this.module_name = module_name;
	}
	public Long getModule_access_num() {
		return module_access_num;
	}
	public void setModule_access_num(Long module_access_num) {
		this.module_access_num = module_access_num;
	}
}
